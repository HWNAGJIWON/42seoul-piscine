int			ft_atoi(char *str)
{
	int		res;
	int		n;

	n = 1;
	res = 0;
	while ((9 <= *str && *str <= 13) || *str == 32)
		str++;
	while (*str == '+' || *str == '-')
	{
		if (*str == '-')
			n *= -1;
		str++;
	}
	while ('0' <= *str && *str <= '9')
	{
		res = (res * 10) + (*str - 48);
		str++;
	}
	return (res * n);
}
