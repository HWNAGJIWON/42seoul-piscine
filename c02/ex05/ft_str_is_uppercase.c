int		ft_str_is_uppercase(char *str)
{
	int		i;

	i = 0;
	while (str[i] != 0)
	{
		if (!('A' <= str[i] && str[i] <= 'Z'))
			return (0);
		i++;
	}
	if (str[i] == 0)
		return (1);
	return (1);
}
