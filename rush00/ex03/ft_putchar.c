#include <unistd.h>

extern void	ft_putchar(int i, int j, int x, int y)
{
	if (i == 1 && (j == 1 || j == y))
	{
		write(1, &"A", 1);
	}
	else if (i == x && (j == 1 || j == y))
	{
		write(1, &"C", 1);
	}
	else if ((i == 1) || (i == x) || (j == 1) || (j == y))
	{
		write(1, &"B", 1);
	}
	else
	{
		write(1, &" ", 1);
	}
}
